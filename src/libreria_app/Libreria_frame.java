    public static void llenarComboBoxPersonas(ArrayList<Persona> arrayList, JComboBox comboBox){
        comboBox.removeAllItems();
        comboBox.addItem("Seleccionar");
        for(Persona xx: arrayList){
            comboBox.addItem(xx.getNombre());
        }
    }
    
    public static void llenarComboBoxAutores(ArrayList<Autor> arrayList, JComboBox comboBox){
        for(Autor xx: arrayList){
            comboBox.addItem(xx.getNombre());
        }
    }
    
    public static void llenarComboBoxLibros(ArrayList<Libro> arrayList, JComboBox comboBox){
        comboBox.removeAllItems();
        comboBox.addItem("Seleccionar");
        for(Libro xx: arrayList){
            comboBox.addItem(xx.getNombre());
        }
    }
    
