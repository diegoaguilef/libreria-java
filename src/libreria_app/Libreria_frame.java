    private void cmbLibrosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmbLibrosActionPerformed
        // TODO add your handling code here:
        String nombre_libro = String.valueOf(cmbLibros.getSelectedItem());
        Libro libro = DatosLibro.buscarLibro(nombre_libro);
        String editorial = libro.getEditorial();
        String anio = String.valueOf(libro.getAnio_publicacion());
        String autor = libro.getAutor().getNombre();
        String stock = String.valueOf(libro.getStock());
        
        txtInfoNombre.setText(nombre_libro);
        txtInfoEditorial.setText(editorial);
        txtInfoAnio.setText(anio);
        txtInfoAutor.setText(autor);
        txtInfoStock.setText(stock);
    }//GEN-LAST:event_cmbLibrosActionPerformed

