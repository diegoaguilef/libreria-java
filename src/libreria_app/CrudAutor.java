  public boolean validarFecha(String fecha){
    boolean valid = false;
    if((fecha != null && fecha.split("/").length == 3) ||
      (fecha != null && fecha.split("-").length == 3)){
      valid = true;
    }
    return valid;
  }
  
  public String[] parsearFecha(String fecha){
    String[] f = {};
    if(fecha.split("/").length == 3){
      f = fecha.split("/");
    }else{
      f = fecha.split("-");
    }
    return f;
  }
  
