  private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
    // TODO add your handling code here:
    String nombre = "";
    int dia, mes, anio;
    Fecha fecha_nacimiento;
    String pais = "";    
    if(!txtNombre.getText().equals("") ||
      !validarFecha(txtFecha.getText()) ||
      !txtPais.getText().equals("")){
      String[] fecha = parsearFecha(txtFecha.getText());
      nombre = txtNombre.getText();
      dia = Integer.parseInt(fecha[0]);
      mes = Integer.parseInt(fecha[1]);
      anio = Integer.parseInt(fecha[2]);
      fecha_nacimiento = new Fecha(dia, mes, anio);
      pais = txtPais.getText();
      
      Autor autor = new Autor(nombre, pais, fecha_nacimiento);
      
      if(DatosAutor.editar(autor)){
        areaListado.setText(areaListado.getText() + autor.toString() + "\n");
        JOptionPane.showMessageDialog(null, "Autor Editado Correctamente");
      }else{
        JOptionPane.showMessageDialog(null, "Error al Editar Autor");
      }
    }else{
      JOptionPane.showMessageDialog(null, "No pueden haber campos vacios");
    }
  }//GEN-LAST:event_btnEditActionPerformed

  private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
    // TODO add your handling code here:
    String nombre = "";
    int dia, mes, anio;
    Fecha fecha_nacimiento;
    String pais = "";
    int id;
    
    if(!txtNombre.getText().equals("") ||
      !validarFecha(txtFecha.getText()) ||
      !txtPais.getText().equals("")){
      String[] fecha = parsearFecha(txtFecha.getText());
      nombre = txtNombre.getText();
      dia = Integer.parseInt(fecha[0]);
      mes = Integer.parseInt(fecha[1]);
      anio = Integer.parseInt(fecha[2]);
      fecha_nacimiento = new Fecha(dia, mes, anio);
      pais = txtPais.getText();
      Autor autor = new Autor(nombre, pais, fecha_nacimiento);
      if(DatosAutor.agregar(autor)){
        Autor ultimo = DatosAutor.getAutores().get(DatosAutor.getAutores().size() - 1);
        txtId.setText(String.valueOf(ultimo.getId()));
        areaListado.setText(areaListado.getText() + autor.toString() + "\n");
        JOptionPane.showMessageDialog(null, "Autor creado Correctamente");
      }else{
        JOptionPane.showMessageDialog(null, "Error al crear Autor");
      }
    }else{
      JOptionPane.showMessageDialog(null, "No pueden haber campos vacios");
    }
  }//GEN-LAST:event_btnCreateActionPerformed

  private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
    // TODO add your handling code here:
    String nombre = "";
    int id;
    if(!txtNombre.getText().equals("") || !txtId.getText().equals("")){
      nombre = txtNombre.getText();
      id = Integer.parseInt(txtId.getText());
      Autor autor = DatosAutor.buscarId(id);
      if(autor != null){
        txtNombre.setText(autor.getNombre());
        txtFecha.setText(autor.getFechaNacimiento().toString());
        txtPais.setText(autor.getPais());
        txtId.setText(String.valueOf(autor.getId()));
        JOptionPane.showMessageDialog(null, "Autor encontrado");
      }else{
        JOptionPane.showMessageDialog(null, "Autor no encontrado");
      }
    }else{
      JOptionPane.showMessageDialog(null, "No puede quedar nombre vacio");
    }
  }//GEN-LAST:event_btnSearchActionPerformed

  private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
    // TODO add your handling code here:
    int id;
    if(!txtId.getText().equals("")){
      id = Integer.parseInt(txtNombre.getText());
      
      if(DatosAutor.eliminar(id)){
        resetFields();
        JOptionPane.showMessageDialog(null, "Autor eliminado");
      }else{
        JOptionPane.showMessageDialog(null, "Error al Eliminar o no encontrado");
      }
    }else{
      JOptionPane.showMessageDialog(null, "No puede quedar nombre vacio");
    }
  }//GEN-LAST:event_btnDeleteActionPerformed

